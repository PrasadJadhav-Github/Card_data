package com.gadre.card_data.Model;

public class Features {
    private String Bluetooth;
    private String Backup_Camera;
    private String Keyless_Entry;

    public String getBackup_Camera() {
        return Backup_Camera;
    }

    public void setBackup_Camera(String backup_Camera) {
        Backup_Camera = backup_Camera;
    }

    public String getBluetooth() {
        return Bluetooth;
    }

    public void setBluetooth(String bluetooth) {
        Bluetooth = bluetooth;
    }

    public String getKeyless_Entry() {
        return Keyless_Entry;
    }

    public void setKeyless_Entry(String keyless_Entry) {
        Keyless_Entry = keyless_Entry;
    }


}
